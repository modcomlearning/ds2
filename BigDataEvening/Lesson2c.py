
# using elif  - else if
# used for multiple conditions

marks = float(input('What marks did you get:'))

if marks <= 30:
    print('You Failed')

elif marks > 30 and marks <= 50:
    print('You got an average mark')

elif marks > 50 and marks <= 75:
    print('You got above average')

elif marks > 75 and marks <= 100:
    print('Bravo!')













